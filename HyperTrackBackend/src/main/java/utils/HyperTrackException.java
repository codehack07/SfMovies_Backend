package main.java.utils;

public class HyperTrackException extends Exception{

	public HyperTrackException(){
	}
	
	public HyperTrackException(String s){  
		super(s);  
	}  
	
	public HyperTrackException(Throwable cause){
		super(cause);
	}

	public HyperTrackException(String message, Throwable cause){
		super(message, cause);
	}

	public HyperTrackException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace){
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
